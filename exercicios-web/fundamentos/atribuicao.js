const a = 7
let b = 3

b += a // equivalente a b = b + a // simbolo de atribuição somatoria
console.log(b)

b -= 4 // b = b - 4
console.log(b) 

b *= 2 // b = b * 2
console.log(b)

b /= 2 // b = b / 2
console.log(b)

b %= 2 // b = b % 2 // representa o resto da divisão, sendo 0 par, e 1 impar
console.log(b)